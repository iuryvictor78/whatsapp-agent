/**
 * Analisa mensagem localmente sem dependência de API
 */
async function analyzeMessageLocal(content) {
  const lowerContent = content.toLowerCase();

  // Contar palavras-chave bloqueadas
  let blockedCount = 0;
  let filteredCount = 0;

  for (const keyword of BLOCKED_KEYWORDS) {
    if (lowerContent.includes(keyword)) {
      blockedCount++;
    }
  }

  for (const keyword of FILTER_KEYWORDS) {
    if (lowerContent.includes(keyword)) {
      filteredCount++;
    }
  }

  // Lógica de classificação
  if (blockedCount >= 1) {
    return {
      status: "blocked",
      reason: "Mensagem contém linguagem agressiva, xingamentos ou ataques pessoais que violam a política de comunicação respeitosa.",
    };
  }

  if (filteredCount >= 2 || (lowerContent.length > 150 && filteredCount >= 1)) {
    // Sugerir versão filtrada
    let filteredContent = content;

    // Remover palavras-chave de vitimização
    for (const keyword of FILTER_KEYWORDS) {
      const regex = new RegExp(`\\b${keyword}\\b`, "gi");
      filteredContent = filteredContent.replace(regex, "");
    }

    // Limpar espaços extras
    filteredContent = filteredContent.replace(/\s+/g, " ").trim();

    // Se ficou muito curto, tentar outra abordagem
    if (filteredContent.length < 10) {
      filteredContent = content.split(/[!?.]/)[0].trim();
    }

    return {
      status: "filtered",
      reason: "Mensagem contém tom agressivo, desabafos emocionais ou vitimização. Sugerimos uma versão mais neutra e focada no bem-estar da criança.",
      filteredContent: filteredContent || content,
    };
  }

  // Mensagem aprovada
  return {
    status: "approved",
    reason: "Mensagem neutra, respeitosa e focada no bem-estar da criança.",
  };
}

module.exports = { analyzeMessageLocal };
